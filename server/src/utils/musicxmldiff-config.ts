/**
 * Validates a MusicXML color string (#RRGGBB or #AARRGGBB).
 * Returns true if valid, false otherwise.
 */
export function isValidMusicXMLColor(color: string): boolean {
  return /^#[0-9A-Fa-f]{6}([0-9A-Fa-f]{2})?$/.test(color);
}
